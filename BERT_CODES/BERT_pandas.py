def df_to_dataset(dataframe, batch_size):
    labels = tf.squeeze(tf.constant([dataframe.pop('label')]), axis=0)
    ds = tf.data.Dataset.from_tensor_slices((dataframe, labels)).batch(
        batch_size)
    return ds

